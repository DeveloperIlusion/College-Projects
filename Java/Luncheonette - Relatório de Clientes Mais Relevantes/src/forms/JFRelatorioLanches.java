    // preenche a tabela do relatório com os dados da view
    private void preenchaTabela() {

        // recupera o objeto visual da tabela
        DefaultTableModel model = (DefaultTableModel) tblRelatorio.getModel();

        // limpa a tabela
        model.setRowCount(0);

        // cria um objeto da classe DAO (Lanche)
        LancheDAO dao = new LancheDAO();

        // preenche a coleção de objetos de relatório de lanche
        this.lanches = dao.listeRelatorioLanche();

        // percorre todos os objetos da classe de relatório de lanche
        for (RelatorioLanche l : this.lanches) {
            // adiciona uma linha na tabela
            model.addRow(new String[]{
                l.getLanche(),
                Integer.toString( l.getVendidos() ),
                Float.toString( l.getPrecoVenda() ).replace(".", ","),
                Float.toString( l.getFaturamento() ).replace(".", ",")
            });
        }
    }

