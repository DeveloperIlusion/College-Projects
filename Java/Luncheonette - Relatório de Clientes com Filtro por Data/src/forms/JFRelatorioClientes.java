    // preenche a tabela do relatório com os dados da view
    private void preenchaTabela() {

        // recupera o objeto visual da tabela
        DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();

        // limpa a tabela
        model.setRowCount(0);

        // cria um objeto da classe DAO (Lanche)
        ClienteDAO dao = new ClienteDAO();

        // preenche a coleção de objetos de relatório de lanche
        this.clientes = dao.listeRelatorioCliente();

        // percorre todos os objetos da classe de relatório de lanche
        for (RelatorioClientes c : this.clientes) {
            // adiciona uma linha na tabela
            model.addRow(new String[]{
                c.getNome(),
                Float.toString( c.getTotalGasto() ).replace(".", ",")
            });
        }
    }

