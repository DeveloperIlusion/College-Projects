    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        try {
            
            // recupera a tabela (objeto)
            DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();

            // limpa a tabela
            model.setRowCount(0);

            // cria um objeto da classe lancheDAO
            ClienteDAO dao = new ClienteDAO();

            // recupera as datas informadas
            Date dataInicial = new SimpleDateFormat("dd/MM/yyyy").parse(
                txtDataInicial.getText()
            );

            Date dataFinal = new SimpleDateFormat("dd/MM/yyyy").parse(
                txtDataFinal.getText()
            );

            // preenche o arraylist de relatório
            this.clientes = dao.filtreRelatorioLanche(dataInicial, dataFinal);

            // percorre todos os objetos da classe de relatório
            for (RelatorioClientes c : this.clientes) {
                // adiciona uma nova linha na tabela
                model.addRow(new String[]{
                    c.getNome(),
                    Float.toString(c.getTotalGasto()).replace(".", ",")
                });
            }
        } catch (ParseException ex) {
            
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

